/** 富文本块类型。 */
export type LegalBlock =
  | { type: 'p'; text: string }
  | { type: 'ul'; items: string[] }
  | { type: 'callout'; text: string };

/** 文档章节。 */
export interface LegalSection {
  /** 锚点 id（#s1...），用于目录跳转 */
  id: string;
  /** 序号 */
  num: number;
  /** 章节标题 */
  title: string;
  /** 章节正文块 */
  blocks: LegalBlock[];
}

/** 整篇法律文档。 */
export interface LegalDocument {
  /** 顶部 pill 文案 */
  pill: string;
  /** 文档标题 */
  title: string;
  /** 文档导语 */
  subtitle: string;
  /** 更新/生效元信息 */
  meta: string;
  /** 章节列表 */
  sections: LegalSection[];
}

